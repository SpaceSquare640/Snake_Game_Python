export function keyLabel(k) {
  if (!k) return "?";
  if (k === " ") return "Space";
  if (k.startsWith("Arrow")) return k.slice(5);
  return k.length === 1 ? k.toUpperCase() : (k[0].toUpperCase() + k.slice(1));
}
